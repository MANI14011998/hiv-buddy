            	<h3 class="header-title">PREGNANCY, CHILDBIRTH & BREASTFEEDING AND HIV</h3>

                <h4 class="secondary-header-title">How is HIV transmitted from mother-to-baby?</h4>

                <p>If you are a pregnant woman living with HIV, HIV in your blood could pass into your baby’s body. This is most likely to occur in the last few weeks of pregnancy, during labour, or delivery. Breastfeeding your baby can also transmit HIV, because HIV is found in your breastmilk.</p>
                <p>There is a 15-45% chance of passing HIV to your baby if neither of you take HIV treatment.</p>

                <h4 class="secondary-header-title">How do I know if I have HIV?</h4>
                <p>If you are pregnant, it is important to attend your antenatal appointments, as this is where you can get an HIV test.</p>
                <p>You will be offered a test at your first appointment. If the result comes back positive you will be encouraged to start treatment straight away. You will also be offered a test in your third trimester (from 28 weeks).</p>
                <p>If at any point during your pregnancy or breastfeeding stage you think you have been exposed to HIV, you may be able to take post-exposure prophylaxis (PEP). PEP has to be taken within 72 hours and prevents HIV from establishing in your body and being passed to your baby. If you’re breastfeeding, you should discuss whether or not to continue with your healthcare professional.</p>

                <h4 class="secondary-header-title"></h4>
                <p>If your HIV test result comes back positive, there are a number of things you can do to reduce the risk of passing HIV to your baby.</p>
                <p><b>Taking treatment to protect your baby</b></p>
                <p>Taking treatment properly can reduce the risk of your baby being born with HIV to less than 1%.</p>
                <p>If you found out that you are HIV-positive before you got pregnant, you may be taking treatment already. If you are not, talk to a healthcare professional about starting treatment straight away.</p>
                <p>If you found out that you are HIV-positive during your pregnancy, it is recommended that you start treatment immediately and continue taking it every day for life.</p>
                <p>Your baby will also be given treatment for four to six weeks after they are born to help prevent an HIV infection developing.</p>
                <p><b>Protecting your baby during childbirth</b></p>
                <p>If you take your treatment correctly, it will lower the amount of HIV in your body so much that it is said to be ‘undetectable’ (undetectable viral load).</p>
                <p>This means that you can plan to have a normal, vaginal delivery because the risk of passing HIV to your baby during childbirth will be extremely small.</p>
                <p>If you don’t have an undetectable viral load, you may be offered a caesarean section, as this carries a smaller risk of passing HIV to your baby than a vaginal delivery.</p>
                <p><b>Protecting your baby during breastfeeding</b></p>

                <p>Breast milk contains HIV. However, guidelines on whether to breastfeed vary depending on what resources are available to you.</p>
                <p>If you always have access to formula and clean, boiled water, you should not breastfeed and give formula instead.</p>
                <p>If you do not have access to formula and clean, boiled water all of the time, you may be advised to breastfeed while both you and your baby are taking antiretroviral treatment.
                </p>
                <p>If you do breastfeed, you must always take your treatment and exclusively breastfeed (give breast milk only) for at least 6 months. Mixing breast milk and other foods before this time increases your baby’s risk of HIV. You can mix-feed your baby after 6 months.</p>
                <p>If you are unsure whether to breastfeed or not, talk to a healthcare professional for more specialist advice.
                    <h4 class="secondary-header-title">Does my baby have HIV?</h4>
                    <p>Your baby should be tested for HIV at birth, and again four to six weeks later.</p>
                    <p>If the result comes back negative, your baby should be tested again at 18 months and/or when you have finished breastfeeding to find out your baby’s final HIV status.</p>
                    <p>If any of these tests come back positive, your baby will need to start treatment straight away. Talk to your healthcare professional, and attend follow-up appointments to ensure your baby receives treatment.


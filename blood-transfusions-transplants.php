            	<h3 class="header-title">BLOOD TRANSFUSIONS & TRANSPLANTS AND HIV</h3>

                <h4 class="secondary-header-title">How is HIV passed on by blood transfusions and transplants?</h4>

                <p>If a person who is HIV-positive donates blood products, such as an organ or tissue, the person who receives the blood product is likely to develop an HIV infection too.</p>
                <p>To prevent this, blood products are tested for HIV before they are given to anyone.</p>

                <h4 class="secondary-header-title">Is all donated blood tested for HIV?</h4>
                <p>International guidelines state that all blood products must be tested for viruses such as HIV.</p>
                <p>High-income countries test and screen all blood products which will identify those that need to be disposed of if they contain HIV.</p>
                <p>Some low-income countries lack the equipment to test all blood, so there have been some examples of donated blood products containing HIV. However, this is still very rare and people who donate blood are often asked questions that will help determine if they have been at risk of HIV infection in the past.</p>
                
                <h4 class="secondary-header-title">How do I know if the blood transfusion/transplant I’m receiving is safe?</h4>
                <p>In most cases, the blood product you are receiving is safe. However if you are worried, it is your right to ask the healthcare professional if it has been tested for HIV or not.</p>

                <h4 class="secondary-header-title">Who can donate blood products?</h4>
                <p>In some countries, certain groups of people are banned from donating blood products for a specific period of time because they are more likely to be HIV-positive.</p>
                <p>The main reason for the ban is that they may be infected with HIV but it hasn’t shown up on an HIV test yet (it can take up to 3 months after infection for HIV to show up on a test).</p>
                <p>Groups of people that may be banned for some time or for life include men who have sex with men, sex workers, and people who inject drugs. Certain activities such as having a tattoo or body piercing, or if you are living with a certain health condition may also mean you can’t donate blood for a while.</p>
                <p>Check the guidelines in your country as they are different all over the world.</p>
                <p>If you fall under one of these groups of people, tell a healthcare professional and they can advise you whether it’s safe to donate blood or not.</p>

                <h4 class="secondary-header-title">Can I get HIV from donating blood?</h4>
                <p>No. New, disposable and sterile needles will be used to collect your blood, meaning there will be no blood from an HIV-positive person on the needle.</p>
                <p>If you suspect that the needle your healthcare worker is using is not new or sterile then ask them to change the needle before agreeing to give blood.</p>



            	<h3 class="header-title">How Do You Get HIV?</h3>
                <p>Despite what you may have heard, there are only a few ways you can get HIV. Here, we explain the ways you can get it and how to protect yourself from HIV infection.</p>
                <h4 class="secondary-header-title">How can you get HIV?</h4>
                <p class="secondary-header-title">HIV lives in the following bodily fluids of an infected person:</p>
                <ul>
                    <li>blood</li>
                    <li>semen and pre-seminal fluid (“pre-cum”)</li>
                    <li>rectal fluids/anal mucous</li>
                    <li>vaginal fluids</li>
                    <li>breast milk.</li>
                </ul>
                <p>To get infected, these bodily fluids need to get into your blood through a mucous membrane (for example the lining of the vagina, rectum, the opening of the penis, or the mouth), breaks in the skin (like cuts), or be injected directly into your bloodstream.</p>
                <p>A person living with HIV can pass the virus to others whether they have symptoms or not. People with HIV are most infectious in the first few weeks after infection.</p>
                
                <h4 class="secondary-header-title">How can’t you get HIV?</h4>
                <p>Some people wrongly believe that HIV can be spread by the air (even though HIV can’t survive outside the body) and other ways such as by touching toilet seats or from mosquito bites.</p>
                <p>See our page on <a href="">HIV myths</a> for more information.</p>




                

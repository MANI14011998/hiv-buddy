            	<h3 class="header-title">MYTHS ABOUT HIV AND AIDS</h3>

                <p>There are lots of myths about how you can get HIV. Here we discuss those myths to make sure you know the truth about how HIV is passed on.</p>
                <p>HIV is passed on from person to person if infected body fluids (such as blood, semen, vaginal or anal secretions and breast milk) get into your bloodstream. The five main ways this can happen are:</p>
                <ul>
                    <li>unprotected sex</li>
                    <li>from mother to child during pregnancy, childbirth or breastfeeding</li>
                    <li>injecting drugs with a needle that has infected blood in it</li>
                    <li>infected blood donations or organ transplants</li>
                    <li>a healthcare worker who gets the blood of an infected patient inside their body.</li>
                </ul>

                <h4 class="secondary-header-title">You cannot get HIV from…</h4>
                <p><b>Someone who doesn’t have HIV</b></p>
                <p>You can only get HIV from someone who is already infected with HIV.</p>
                <p><b>Touching someone who has HIV</b></p>
                <p>HIV can’t survive outside of the body so you won’t get HIV from touching someone, hugging them or shaking their hand.</p>
                <p><b>Sweat, tears, urine or faeces of someone who has HIV</b></p>
                <p>There is no HIV in an infected person’s sweat, tears, urine or faeces.</p>
                <p><b>Mutual masturbation</b></p>
                <p>Mutual masturbation, fingering and hand-jobs are all safe from HIV. However, if you use sex toys make sure you use a new condom on them when switching between partners.</p>
                <p><b>Insects</b></p>
                <p>You cannot get HIV from insects. When an insect (such as a mosquito) bites you it sucks your blood – it does not inject the blood of the last person it bit.</p>
                <p><b>Air</b></p>
                <p>HIV cannot survive in the air so coughing, sneezing or spitting cannot transmit HIV.</p>
                <p><b>New or sterilised needles</b></p>
                <p>New needles cannot transmit HIV because they haven’t been in the body of an infected person. If used needles are cleaned and sterilised properly they can’t transmit HIV either.</p>
                <p><b>Water</b></p>
                <p>HIV can’t survive in water, so you won’t get HIV from swimming pools, baths, shower areas or from drinking water.</p>
                <p><b>Toilet seats, tables, door handles, cutlery, sharing towels</b></p>
                <p>HIV doesn’t survive on surfaces, so you can’t get HIV from any of these.</p>
                <p><b>Musical instruments</b></p>
                <p>HIV can’t survive on musical instruments. Even if it is an instrument that you play using your mouth, it can’t give you HIV.</p>
                <p><b>Used condoms</b></p>
                <p>HIV can only survive for a really short amount of time outside of the body. Even if the condom had sperm from an HIV-positive person in it, the HIV would be dead.</p>
                <p><b>Kissing</b></p>
                <p>There is such a small amount of HIV in the saliva of an infected person that HIV can’t be passed on from kissing. There is only a risk if you both have large open sores or bleeding gums and blood is exchanged.</p>
                <p><b>Oral sex</b></p>
                <p>As with kissing, the risk of HIV from oral sex is so small unless you or your partner have large open sores on the genital area or bleeding gums/sores in your mouth. Always use a condom or dental dam to eliminate the risk.</p>
                <p><b>Tattoos and piercings</b></p>
                <p>There is only a risk if the needle used by the professional has been used in the body of an HIV-infected person and not sterilised afterwards.</p>

                <h4 class="secondary-header-title">If I get infected fluid from an HIV-positive person into my body will I definitely get HIV?</h4>
                <p>No, HIV is not always passed on from an infected person. There are lots of reasons why this is the case. For example, if the HIV-positive person is on treatment it will reduce the amount of HIV in their body meaning it is unlikely to be passed on.</p>
                <p>Also, if you get infected blood into your body you may be able to take post-exposure prophylaxis (PEP), which stops the virus from becoming an infection. However it’s not available everywhere and has to be taken within 72 hours to be effective.</p>
                <p>It’s really important to always take a HIV test if you think you have been at risk of HIV.</p>

                <h4 class="secondary-header-title">Isn’t HIV only a risk for certain groups of people?</h4>
                <p>Some people think that only certain groups of people can get HIV but they are wrong.</p>
                <p>Everyone is at risk of HIV if you get HIV into your bloodstream via one of the ways mentioned above.
                <p>Some people have a higher risk of getting HIV because they engage in certain activities (e.g. injecting drugs) that are more likely to transmit the virus, or they have lots of sex partners and don’t use a condom.</p>

                <h4 class="secondary-header-title">I’m HIV-positive and so is my partner so we don’t have to worry about HIV do we?</h4>
                <p>Wrong. There are many strains of the HIV virus. If you get infected with 2 or more strains of HIV it can cause problems for your treatment. Make sure you are still using condoms if you and your partner are living with HIV.</p>

                <h4 class="secondary-header-title">It’s easy to tell the symptoms of HIV…</h4>
                <p>Wrong. The symptoms of HIV can differ from person-to-person and some people may not get any symptoms at all. Without treatment, the virus will get worse over time and damage your immune system. There are three stages of HIV infection with different possible effects.</p>


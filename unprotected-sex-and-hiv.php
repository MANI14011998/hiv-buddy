            	<h3 class="header-title">Unprotected Sex and HIV</h3>

                <p>Unprotected sex means having sex (vaginal, anal or oral) without using a condom. It means you are at risk of HIV as well as sexually transmitted infections (STIs).</p>

                <h4 class="secondary-header-title">How do you get HIV from unprotected sex?</h4>
                <p>During unprotected sex, HIV in the bodily fluids of an infected person (blood, semen, vaginal fluid, pre-cum or anal mucus) can pass into the body of their sexual partner. This can happen through the mucous membranes of the penis, vagina, rectum and sometimes the mouth and throat.</p>
                <p>If someone is living with HIV, they are more likely to pass it on to others in the first few months after infection as there are high levels of the virus in their bodily fluids at this point.</p>
                
                <h4 class="secondary-header-title">Are some types of sex riskier than others?</h4>
                <p>Most people get HIV by having unprotected vaginal or anal sex. Anal sex is the most risky because the lining of the anus is more delicate than the lining of the vagina and is more easily damaged. Receptive anal sex (“bottoming”) is riskier than insertive anal sex (“topping”).</p>
                <p>There is a very small chance of getting HIV from unprotected oral sex, but only if the person giving oral sex has mouth ulcers, sores or bleeding gums, or if the person receiving oral sex has been recently infected.</p>
                <p>Having multiple sexual partners and/or STIs also increases the risk of HIV infection via unprotected sex.</p>

                <h4 class="secondary-header-title">How can I avoid getting HIV from unprotected sex?</h4>
                
                <p><b>Condoms</b> are the most effective way of preventing HIV (and STI) infection via vaginal and anal sex as well as oral sex performed on men. They should be put on before any sexual contact as HIV can be passed on through pre-come, vaginal fluid, and from the anus.</p>
                <p><b>Lubricants</b>, or lube, make sex safer by reducing the risk of vaginal or anal tears caused by dryness or friction. It can also reduce the risk of a condom breaking.</p>
                <p>Use water-based lubricants instead of oil-based lubricants (such as Vaseline) as oil-based lubricants weaken the latex in condoms and can cause them to break.</p>

                <p><b>A dental dam</b> is a small plastic sheet that can be used to cover the mouth and the vagina or anus to reduce the risk of HIV (and STIs) during oral sex.</p>
                <p><b>Pre-exposure prophylaxis (PrEP)</b></p>
                <p><b>PrEP</b> is antiretroviral drugs taken daily by people at high risk of HIV infection. To prevent sexual transmission of HIV, PrEP is sometimes recommended for:</p>

                <ul>
                    <li>anyone in an ongoing relationship with an HIV-positive partner</li>
                    <li>anyone who is sexually active with more than one person, even if they recently tested negative for HIV</li>
                    <li>heterosexual men or women who don't use condoms with partners whose HIV status is unknown and are at high risk of HIV infection (e.g. they inject drugs or have bisexual male partners).</li>

                </ul>
                <p>PrEP can provide a high level of protection against HIV, but is most effective when used with condoms. PrEP is not available everywhere.</p>

                <p><b>If you think you are at high risk of HIV infection via unprotected sex, talk to a healthcare professional about whether PrEP is right for you.</b></p>
                <p><b>Post-exposure prophylaxis (PEP)</b></p>
                <p><b>PEP is the use of antiretroviral drugs after an event that puts you at risk of HIV - such as unprotected sex - to stop HIV from spreading in the body. It must be started within 72 hours of possible exposure.</b></p>
                <p>However, PEP is not 100% effective, and should not be viewed as an alternative to condoms.</p>
                <p><b>Your doctor or healthcare worker will advise you on whether you could take PEP. Again, PEP may not be available where you are.</b></p>

                
